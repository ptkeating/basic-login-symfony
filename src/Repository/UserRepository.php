    /**
     * Find a user by email
     * @param $value - the email to search for
     * 
     * @return \App\Entity\User
     */
    public function findByEmail($value): ?User
    {
        return $this->createQueryBuilder('u')
            ->andWhere('u.email = :val')
            ->setParameter('val', $value)
            ->getQuery()
            ->getOneOrNullResult();
    }

